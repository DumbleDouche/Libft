#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	char	*ret;

	ret = NULL;
	if (!s)
		return ;
	if (s || n > 0)
	{
		ret = s;
		while (n--)
			*ret++ = '\0';
	}
}
