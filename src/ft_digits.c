#include "../includes/libft.h"

size_t		ft_digits(long nbr)
{
	int i;

	i = 0;
	if (nbr < 0 && ++i)
		nbr = ABS(nbr);
	while (nbr /= 10)
		++i;
	++i;
	return (i);
}
